import { useEffect, useState } from "react";
import Footer from "../layout/Footer";
import NavBar from "../layout/NavBar";
import getUserFromJwt from "../../helper/getAccessToken";
import LoadingBar from "react-top-loading-bar";

function AboutPage() {
  const [user, setUser] = useState(null);
  const [progress, setProgress] = useState(0);

  const accessToken = localStorage.getItem("accessToken");

  useEffect(() => {
    const fetchUser = async () => {
      try {
        setProgress(50);
        const userData = await getUserFromJwt(accessToken);
        setUser(userData);
        setProgress(100);
      } catch (error) {
        console.error("Error fetching user data:", error);
      }
    };
    if (accessToken) fetchUser();
  }, [accessToken]);

  return (
    <div>
      <LoadingBar color="#f11946" progress={progress} height={3} />
      <NavBar user={user} />
      <div className="container py-4">
        <h2>
          <b>Welcome to Quill Quest: Your Tech Exploration Hub</b>
        </h2>
        <p>
          Welcome to Quill Quest, your premier destination for all things tech!
          Whether youre a seasoned developer, a tech enthusiast, or simply
          curious about the latest innovations, Quill Quest is your ultimate
          guide to navigating the dynamic world of technology.
        </p>
        <h4>
          <b>Our Mission</b>
        </h4>
        <p>
          At Quill Quest, our mission is simple: to empower individuals with the
          knowledge, insights, and resources they need to thrive in the
          ever-evolving landscape of technology. We believe that technology has
          the power to transform lives, shape industries, and drive positive
          change in the world. Through our articles, tutorials, and guides, we
          aim to demystify complex concepts, spark meaningful discussions, and
          inspire our readers to explore, create, and innovate.
        </p>
        <h4>
          <b>What Sets Us Apart</b>
        </h4>
        <ul>
          <li>
            Expert Insights: Our team of experienced writers, industry
            professionals, and tech enthusiasts bring you expert insights,
            analysis, and commentary on the latest trends, developments, and
            innovations in the tech industry.
          </li>
          <li>
            Comprehensive Coverage: From programming languages to artificial
            intelligence, from cybersecurity to blockchain, we cover a wide
            range of topics to cater to tech enthusiasts of all levels and
            interests.
          </li>
          <li>
            Practical Resources: Whether youre looking for coding tutorials,
            troubleshooting guides, or career advice, Quill Quest provides
            practical resources and actionable tips to help you level up your
            tech skills and advance your career.
          </li>
        </ul>
        <h4>
          <b>Get Involved</b>
        </h4>
        <ul>
          <li>
            Read Our Articles: Dive into our extensive collection of articles,
            tutorials, and guides covering a diverse range of tech topics.
            Whether youre a beginner or an expert, theres something for everyone
            at Quill Quest.
          </li>
          <li>
            Join the Community: Connect with fellow tech enthusiasts, share your
            ideas, and participate in discussions on our blog, forums, and
            social media channels. Together, we can learn, grow, and inspire
            each other to reach new heights in the tech world.
          </li>
          <li>
            Contribute Your Expertise: Are you passionate about technology and
            love sharing your knowledge with others? Consider becoming a guest
            contributor and sharing your expertise with our audience. We welcome
            contributions from tech professionals, educators, and enthusiasts
            alike.
          </li>
        </ul>
        <h4>
          <b>Contact Us</b>
        </h4>
        <p>
          Have a question, suggestion, or just want to say hello? Wed love to
          hear from you! Get in touch with us via email, social media, or our
          online contact form. Your feedback is invaluable to us as we strive to
          provide you with the best possible content and user experience. Thank
          you for joining us on this exciting journey through the world of
          technology. Together, lets explore, innovate, and shape the future
          with Quill Quest!
        </p>
        <hr />
        <p>
          Feel free to tailor this content to better reflect the specific focus,
          tone, and goals of your technology blog website.
        </p>
      </div>
      <Footer />
    </div>
  );
}

export default AboutPage;
